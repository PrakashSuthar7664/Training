function invert(arr) {
  for (let i = 0; i < arr.length; i++) {
    // console.log(i);

    for (let j = 0; j < arr[i].length; j++) {
      for (let k = 0; k < arr[i][j].length; k++) {
        // console.log(arr[i][j][k]);

        arr[i][j][k] = 255 - arr[i][j][k];
      }
    }
  }
  console.log(arr);
}

